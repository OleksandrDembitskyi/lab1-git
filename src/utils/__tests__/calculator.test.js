import { describe, it, expect } from 'vitest';
import { add, subtract, multiply, divide } from '../calculator';

describe('Калькулятор', () => {
  // ========== ТЕСТИ ДЛЯ ДОДАВАННЯ ==========
  describe('add', () => {
    it('повинен правильно додавати два додатні числа', () => {
      expect(add(2, 3)).toBe(5);
    });

    it('повинен правильно додавати від\'ємні числа', () => {
      expect(add(-2, -3)).toBe(-5);
    });

    it('повинен правильно додавати додатне і від\'ємне число', () => {
      expect(add(5, -3)).toBe(2);
    });

    it('повинен правильно додавати число з нулем', () => {
      expect(add(5, 0)).toBe(5);
    });
  });

  // ========== ТЕСТИ ДЛЯ ВІДНІМАННЯ ==========
  describe('subtract', () => {
    it('повинен правильно віднімати два числа', () => {
      expect(subtract(5, 3)).toBe(2);
    });

    it('повинен правильно віднімати від\'ємні числа', () => {
      expect(subtract(-2, -3)).toBe(1);
    });

    it('повинен правильно віднімати число від нуля', () => {
      expect(subtract(0, 5)).toBe(-5);
    });
  });

  // ========== ТЕСТИ ДЛЯ МНОЖЕННЯ ==========
  describe('multiply', () => {
    it('повинен правильно множити два додатні числа', () => {
      expect(multiply(2, 3)).toBe(6);
    });

    it('повинен правильно множити на нуль', () => {
      expect(multiply(5, 0)).toBe(0);
    });

    it('повинен правильно множити від\'ємні числа', () => {
      expect(multiply(-2, -3)).toBe(6);
    });

    it('повинен правильно множити додатне на від\'ємне', () => {
      expect(multiply(2, -3)).toBe(-6);
    });
  });

  // ========== ТЕСТИ ДЛЯ ДІЛЕННЯ ==========
  describe('divide', () => {
    it('повинен правильно ділити два числа', () => {
      expect(divide(6, 3)).toBe(2);
    });

    it('повинен правильно ділити від\'ємні числа', () => {
      expect(divide(-6, -3)).toBe(2);
    });

    it('повинен правильно ділити додатне на від\'ємне', () => {
      expect(divide(6, -3)).toBe(-2);
    });

    it('повинен правильно ділити число на 1', () => {
      expect(divide(10, 1)).toBe(10);
    });

    it('повинен викидати помилку при діленні на нуль', () => {
      expect(() => divide(5, 0)).toThrow("Ділення на нуль неможливе");
    });
  });
});